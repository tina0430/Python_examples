# 변수 a = 14와 b = 5가 선언되어 있을 때 변수 sum, multiply, divide, remainder, power 
# 변수들을 만들고 다음과 같이 출력되도록 프로그램을 작성하시오.

a = 14
b = 5
sum = a+b
multiply = a*b 
divide = a/b 
remainder = a%b
power = a**b

print('sum :', sum)
print('multiply :', multiply)
print('divide :', divide)
print('remainder :', remainder)
print('power :', power)
